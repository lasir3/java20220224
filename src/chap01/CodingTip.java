package chap01;

public class CodingTip {
	// hint : ctrl + space
	public static void main(String[] args) {
		
		// 콘솔 출력
		System.out.println();

		// 자동 코드 포맷팅 : ctrl + shift + f
		
		// 콘솔 출력 코드 단축 키워드 : sysout, sout
		System.out.println();
		System.out.println();
		
		// 실행 : ctrl + f11
		// 주석 : ctrl + /
		// 창크기 변경 : ctrl + m
		// 새파일 : ctrl + n
		// 저장 : ctrl + s
	}
}
